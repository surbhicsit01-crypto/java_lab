public class lab1{
    public static void main(String args []){
    int a = 10, b = 5;

        // Arithmetic
        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);

        // Assignment
        a += 2;
        System.out.println(a);

        // Increment / Decrement
        a++;
        System.out.println(a);
        a--;
        System.out.println(a);

        // Comparison
        System.out.println(a > b);
        System.out.println(a == b);

        // Logical
        System.out.println(a > b && b > 0);

        // Bitwise
        System.out.println(a & b);
    }
}
       
    
